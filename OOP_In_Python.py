# (OOP)
# Object-Oriented Programming In Python
from turtle import Turtle, Screen

Object = Turtle()
Object.shape("turtle")
Object.color("green")

Object.forward(100)
Object.left(90)
Object.forward(100)
Object.left(90)
Object.forward(100)
Object.left(90)
Object.forward(100)

my_screen = Screen()
print(my_screen.canvheight)
my_screen.exitonclick()
